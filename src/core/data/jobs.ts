// ---- 生成(数値はCURVES、銘と一言はSEEDS — 単一情報源) ----
function skillsOf(seed: JobSeed): Skill[] {
  return CURVES[seed.role].map((c, i) => ({
    id: `sk_${seed.id}${i + 1}`,
    name: seed.skills[i][0],
    type: c.type,
    target: c.target,
    element: seed.school,
    power: c.power,
    mpCost: c.mpCost,
    inheritable: false,
    desc: seed.skills[i][1],
    // M29修正: 盾(tank)役の家業バフは防御バフ(灯番/壁塗/堰守/垣結=守勢の生業・vit軸)。
    // 他役(癒/支など)のバフは士気・鼓舞寄りのため既定=攻撃上昇のまま。
    buffKind: c.type === 'buff' ? (seed.role === 'tank' ? 'def' : 'atk') : undefined,
  }))
}

export const JOB_CLASSES: JobClass[] = SEEDS.map((s) => ({
  id: s.id,
  name: s.name,
  kana: s.kana,
  role: s.role,
  school: s.school,
  statBias: biasOf(s.role, s.school),
  skillIds: CURVES[s.role].map((_, i) => `sk_${s.id}${i + 1}`),
  ritual: s.ritual,
  desc: s.desc,
}))

export function jobById(id: JobClassId): JobClass {
  const j = JOB_CLASSES.find((x) => x.id === id)
  if (!j) throw new Error(`unknown job: ${id}`)
  return j
}

// 全家業技(skillByIdへの統合用 — toza方式)
export function allJobSkills(): Skill[] {
  return SEEDS.flatMap(skillsOf)
}

// 血潮からの推奨家業(生業の儀のUI補助 — 灯座選択と同じUX文法)
export function recommendJob(potential: Stats): JobRole {
  const entries: [JobRole, number][] = [
    ['atk', potential.str],
    ['tank', potential.vit],
    ['swift', potential.agi],
    ['heal', potential.mnd],
    ['hex', potential.dex],
    ['sup', (potential.mnd + potential.luk) / 2],
  ]
  return entries.sort((a, b) => b[1] - a[1])[0][0]
}
