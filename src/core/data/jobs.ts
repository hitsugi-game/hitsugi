// 血潮補正: 役割の主軸+10、流派の副軸+4(合算)
const ROLE_BIAS: Record<JobRole, Partial<Stats>> = {
  atk: { str: 10 }, tank: { vit: 10 }, swift: { agi: 10 },
  heal: { mnd: 10 }, hex: { dex: 10 }, sup: { mnd: 6, luk: 6 },
}
const SCHOOL_BIAS: Record<JobSchool, Partial<Stats>> = {
  fire: { str: 4 }, earth: { vit: 4 }, water: { mnd: 4 }, wind: { agi: 4 },
}
function biasOf(role: JobRole, school: JobSchool): Partial<Stats> {
  const out: Partial<Stats> = { ...ROLE_BIAS[role] }
  for (const [k, v] of Object.entries(SCHOOL_BIAS[school]) as [keyof Stats, number][]) {
    out[k] = (out[k] ?? 0) + v
  }
  return out
}

