// 誕生の口上
export function birthLine(name: string, godName: string, rng: Rng): string {
  return rng.pick([
    `星が一つ、郷に降りた。${godName}の血を引く子、${name}。`,
    `産声が常夜を少しだけ押し返した。${name}、ここに生まれる。`,
    `${godName}より授かりし新しき灯。その名を${name}という。`,
  ])
}
